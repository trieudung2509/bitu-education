    $(function() {
      let milestoneSlider = $('.con-about-milestone .box-mobile .box-slider');
      milestoneSlider.slick({
        speed: 600,
        autoplaySpeed: 6000,
        autoplay: true,
        infinite: true,
        swipe: true,
        fade: false,
        dots: false,
        arrows: false,
        slidesToShow: 2,
        slidesToScroll: 1
      });

      let maxH = 0;

      const setHeight = () => {
        let item = milestoneSlider.find('.slide-item');
        for(let i=0; i < item.length; i++){
          let h = item[i].clientHeight;
          if(h > maxH) maxH = h;
        }
        if(maxH > 100) {
          item.css('height', maxH);
        }
      // console.log(maxH)
      }
      setHeight();

      window.resize(() => {
        setHeight();
      });
    });
